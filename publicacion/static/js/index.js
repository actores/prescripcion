const listarSeries = async () => {
    try {
        const response = await fetch('http://127.0.0.1:8000/api/listar/series/');
        const data = await response.json();





        let content = ``;
        data.series.forEach((serie, index) => {
            content += `
            <tr>
            <td>${serie.titulo.toUpperCase()}</td>
            <td>${serie.pais.toUpperCase()}</td>
                <td>
                    <a href="detalleExplotacion/${serie.id}" class="btn btn-sm text-white" style="background-color: #ED7D31 !important">Detalle</a>
                </td>
            </tr>
            `
        });

        if (tableBody_series) {

            if (data.series.length > 0) {
                tableBody_series.innerHTML = content
            } else {
                tableBody_series.innerHTML = 'NO HAY REGISTROS'
            }
        }

    } catch (ex) {
        alert(ex)
    }
}


window.addEventListener('load', async () => {
    await initDatatable()
})




